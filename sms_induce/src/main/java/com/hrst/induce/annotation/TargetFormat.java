package com.hrst.induce.annotation;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Created by Lin on 2018/6/27.
 */

@Retention(RetentionPolicy.SOURCE)
@Target({ElementType.PARAMETER, ElementType.FIELD})
public @interface TargetFormat {
    String TARGET_FORMAT_GSM = "GSM";   //GSM制式
    String TARGET_FORMAT_CDMA = "CDMA";  //CDMA制式
    String TARGET_FORMAT_TDSCDMA = "TD-SCDMA";  //TD_SCDMA制式
    String TARGET_FORMAT_WCDMA = "WCDMA";   //WCDMA制式
    String TARGET_FORMAT_LTE = "LTE";   //LTE制式
}
